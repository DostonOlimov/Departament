    public function getAccount()
    {
        return substr($this->paid_acount,0,3) .
            substr($this->paid_acount,4,7) . substr($this->paid_acount,8,11).substr($this->paid_acount,12,15);
    }

    public function trimAccount()
    {
        $this->paid_acount = substr(preg_replace('#[^0-9]#', '', $this->paid_acount),-9);
    }

    public function beforeSave($insert)
    {
        if($this->isNewRecord){
            $this->created_at = time();
            $this->created_by = Yii::$app->user->id;
        }

        $this->updated_at = time();
        $this->updated_by = Yii::$app->user->id;
        if ($this->paid_acount) {
        $this->trimAccount();
        $this->paid_acount = intval(trim($this->paid_acount));
    }
        $this->paid_date = strtotime($this->paid_date);
        $this->decision_date = strtotime($this->decision_date);
        return true;
    }
